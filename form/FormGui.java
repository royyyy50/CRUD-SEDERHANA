    private void TableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableMouseClicked
        int i =Table.getSelectedRow();
        if (i>-1){
            KodeObat.setText(model.getValueAt(i,0).toString());
            NamaObat.setText(model.getValueAt(i,1).toString());
            JenisObat.setText(model.getValueAt(i, 2).toString());
            Stock.setText(model.getValueAt(i, 3).toString());
            Expired.setText(model.getValueAt(i, 4).toString());
            
        }
        
    }//GEN-LAST:event_TableMouseClicked

